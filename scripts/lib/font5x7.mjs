const FALLBACK = G["-"];

export const glyphWidth = 5, glyphHeight = 7;

// measure the pixel width of `text` at scale (with 1px letter spacing * scale).
export function textWidth(text, scale = 1) {
  return [...String(text)].length * (glyphWidth + 1) * scale - scale;
}

// draw `text` at (x,y) top-left into `put(px,py,color)` at integer `scale`. Returns end x.
export function drawText(put, x, y, text, scale, color) {
  let cx = x;
  for (const ch of String(text).toUpperCase()) {
    const g = G[ch] || FALLBACK;
    for (let r = 0; r < glyphHeight; r++)
      for (let c = 0; c < glyphWidth; c++)
        if (g[r][c] === "#")
          for (let sy = 0; sy < scale; sy++) for (let sx = 0; sx < scale; sx++) put(cx + c * scale + sx, y + r * scale + sy, color);
    cx += (glyphWidth + 1) * scale;
  }
  return cx;
}
